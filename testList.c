//determines whether a given list matches the expected data values
static bool compareListToArray(List l, item *data, unsigned long numNodes, bool isEmpty) {

   if (l == NULL) return FALSE;
   if (getNumNodes(l) != numNodes) return FALSE;
   if (isListEmpty(l) != isEmpty) return FALSE;

   //compares all data values in the list with those in the array (expected values)
   for (unsigned long i = 0; i < numNodes; i++) {
      if (getDataAt(l, i) != data[i]) return FALSE;
   }

   return TRUE;
}

